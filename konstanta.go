package main

func main() {
	// Manifest typing
	const firstName string = "Dimas"

	// Type inference
	const lastName = "Ramadhansyah"

	// Multi-variable
	const (
		isMarried bool = false
		angka          // Memiliki tipe data dan value yang sama dengan atasnya
		pi        = 3.14
	)

	const satu, dua int = 1, 2
	const tiga, empat = 3, 4
}

